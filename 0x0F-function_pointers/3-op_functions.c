#include "3-calc.h"
/**
 * op_add - Entry point
 * @a: numero_1
 * @b: numero_2
 * Return: Operaión suma
 */
int op_add(int a, int b)/*Función suma 2 argumentos*/
{
	return (a + b);
}
/**
 * op_sub - Entry point
 * @a: numero_1
 * @b: numero_2
 * Return: Operaión restar
 */
int op_sub(int a, int b)/*Función resta 2 argumentos*/
{
	return (a - b);
}
/**
 * op_mul - Entry point
 * @a: numero_1
 * @b: numero_2
 * Return: Operaión multipliar
 */
int op_mul(int a, int b)/*Función Multiplica 2 argumentos*/
{
	return (a * b);
}
/**
 * op_div - Entry point
 * @a: numero_1
 * @b: numero_2
 * Return: Operaión dividir
 */
int op_div(int a, int b)/*Función divide 2 argumentos*/
{
	return (a / b);
}
/**
 * op_mod - Entry point
 * @a: numero_1
 * @b: numero_2
 * Return: Operaión modulo
 */
int op_mod(int a, int b)/*Función modulo de 2 argumentos*/
{
	return (a % b);
}
